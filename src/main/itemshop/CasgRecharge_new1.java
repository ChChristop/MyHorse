package itemshop;

import java.util.Scanner;

public class CasgRecharge_new1 {

	public static void main(String[] args) {
		int money = 0; 
	
		
		System.out.println("무엇을 하시겠습니까?");
		System.out.println("캐쉬조회(1), 캐취충전(2) 종료(3)");
		Scanner sc = new Scanner(System.in); 
		
		
		for(int i=0;i<100;i++) {// 사용자가 원하는 만큼 횟수를 반복해야 하므로 반복문 사용
			int choice=sc.nextInt(); // 사용자로부터 번호를 입력받기 위한 choice 변수
			
			// 캐쉬조회
			if(choice==1) {
				System.out.println("현재 잔액은 "+money+"원 입니다");
			}
			
			//캐쉬충전
			if(choice==2) {
				System.out.println("얼마를 충전하시겠습니까?");
				int insertMoney=sc.nextInt(); // 사용자가 입금할 금액 
				money+=insertMoney;
				System.out.println(money+ "포인트가 충전되었습니다.");
			}
			
			// 종료 
			if(choice==3) {
				System.out.println("다음에 또 다시 이용해주세요");
				break;
			} 
			
			System.out.println("원하시는 메뉴를 선택해주세요 캐쉬조회(1), 캐취충전(2) 종료(3)");
		}
	}

}

	
	